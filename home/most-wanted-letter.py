def checkio(text):
    """
    Returns the most-occurring letter in `text`, and in the event of a tie,
    the letter that occurs first in the alphabet.
    """
    letters = sorted({l for l in text.lower() if l.isalpha()})
    return max(letters, key=text.lower().count)

